#include "../includes/fdf.h"

int	has_comma(char *str)
{
	int	i;
	int	r;

	i = 0;
	r = 0;
	while (str[i] && r == 0)
	{
		if (str[i] == ',')
			r = 1;
		i++;
	}
	return (r);
}
